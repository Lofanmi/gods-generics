package utils

import (
	"golang.org/x/exp/constraints"
)

// OrderedComparator provides a basic comparison on int
func OrderedComparator[T constraints.Ordered](a, b T) int {
	switch {
	case a > b:
		return 1
	case a < b:
		return -1
	default:
		return 0
	}
}
